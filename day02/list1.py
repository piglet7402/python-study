a = [1, 12, 123, 1234]
a[0:2] = [10, 20]
print(a)

a[0:2] = [10]    # 2개가 1개로 통합됨
# [0:2]는 0 ~ 1을 0 1개로 통합
print(a)

a[1:2] = [20]
print(a)

a[2:3] = [30]
print(a)

b = [1, 12, 123, 1234]
b[1:2] = []
print(b)

b[0:] = []
print(b)

c = [1, 12, 123, 1234]

c[1:1] = ['a']    # 1번째 뒤에 1개 삽입
print(c)

c[5:] = [12345]    # 5번째 뒤에 삽입
print(c)

c[:0] = [-12, -1, 0]    # 0 번째 앞에 삽입
print(c)